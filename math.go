package main

import "fmt"

func main() {

	// operations math
	var result = 10+10
	fmt.Println(result)

	var a = 10
	var b = 20
	var c = a * b

	fmt.Println(c)

	// augmented assignment
	var i = 10
	i += 10 // i = i + 10

	fmt.Println(i)

	// unarty operator
	i++ // i = i + 1
	fmt.Println(i)

	var negative  = -100
	var positif  = +100

	fmt.Println(negative)
	fmt.Println(positif)


}